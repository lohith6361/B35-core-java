package org.tnsindia.operatordemo;

public class IncrementDecrementDemo {

	public static void main(String[] args) {
		//increment
				int a=15,b=6;
				a++;
				int y=a;
				++b;
				System.out.println(b);
				System.out.println(y);
				
				//decrement
				int x=4,z=11;
				--x;
				z--;
				int p=++z;
				System.out.println(x);
				System.out.println(p);

	}

}
